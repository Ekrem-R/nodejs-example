
function addNumbers(a,b){
    return a+b;
}

function subtractNumbers(a,b){
    return a-b;
}

function productNumbers(a,b){
    return a*b;
}
function divideNumbers(a,b){
    return a/b;
}
module.exports={
    add:addNumbers,
    subtract:subtractNumbers,
    product:productNumbers,
    divide:divideNumbers
}